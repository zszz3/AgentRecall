type PromptInput = "input" | "output" | "ground_truth" | "context";

interface PromptDefinition {
  objective: string;
  checks: string[];
  steps: string[];
  anchors: [string, string, string, string, string];
  rules: string[];
  inputs: PromptInput[];
}

const INPUT_TAGS: Record<PromptInput, [string, string]> = {
  input: ["Input", "{{input}}"],
  output: ["Answer", "{{output}}"],
  ground_truth: ["GroundTruth", "{{ground_truth}}"],
  context: ["Context", "{{context}}"],
};

// Structure adapted from OpenEvals (MIT), G-Eval/DeepEval (Apache-2.0),
// and Ragas five-level rubrics (Apache-2.0). Wording is project-specific.
function judgePrompt(definition: PromptDefinition): string {
  const scores = ["0.00", "0.25", "0.50", "0.75", "1.00"];
  const labels = ["不通过", "较差", "一般", "良好", "优秀"];
  return `你是一名严格、公正的模型输出评估员。只评价本 Prompt 指定的维度，不要把其他质量混入本次分数。

<Rubric>
评估目标：${definition.objective}

检查项：
${definition.checks.map((check, index) => `${index + 1}. ${check}`).join("\n")}
</Rubric>

<EvaluationSteps>
${definition.steps.map((step, index) => `${index + 1}. ${step}`).join("\n")}
</EvaluationSteps>

<ScoreAnchors>
${definition.anchors
  .map(
    (description, index) =>
      `${scores[index]}（${labels[index]}）：${description}`,
  )
  .join("\n")}
</ScoreAnchors>

<SpecialRules>
${definition.rules.map((rule) => `- ${rule}`).join("\n")}
</SpecialRules>

<EvaluationData>
${definition.inputs
  .map((input) => {
    const [tag, placeholder] = INPUT_TAGS[input];
    return `<${tag}>\n${placeholder}\n</${tag}>`;
  })
  .join("\n\n")}
</EvaluationData>

<OutputFormat>
只返回 JSON，不要添加 Markdown 或其他文本：
{"score": 0|0.25|0.5|0.75|1, "reason": "结合评分标准给出简洁理由", "evidence": ["支持判断的原文片段"], "failedCriteria": ["未满足的检查项编号"]}
没有证据或失败项时返回空数组。分数必须严格选择五档之一。
</OutputFormat>`;
}

